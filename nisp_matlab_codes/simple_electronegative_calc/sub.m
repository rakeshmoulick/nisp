
function f = sub(psis)
global alpha0 gamma
% ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ 
C1 = exp(-psis) + alpha0*exp(-gamma*psis);
% ------------------------------------------------------------------------
n1ps_ne0 = C1;
% ------------------------------------------------------------------------
fun = n1ps_ne0*(1.0/psis) - 2*exp(-psis) - 2*alpha0*gamma*exp(-gamma*psis);  
% ------------------------------------------------------------------------
f = fun;
end