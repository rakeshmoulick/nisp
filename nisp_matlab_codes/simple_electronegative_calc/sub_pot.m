
function dy = sub_pot(~,y)
global alpha0 gamma psis
dy = zeros(2,1);
% ------------------------------------------------------------------------
C1 = exp(-psis) + alpha0*exp(-gamma*psis);
n1ps_ne0 = C1;
% -------------------------------------------------------------------------
dy(1) = y(2);
dy(2) = n1ps_ne0*sqrt(psis/y(1)) - exp(-y(1)) - alpha0*exp(-gamma*y(1));
% -------------------------------------------------------------------------
end
